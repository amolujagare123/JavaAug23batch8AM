package ControlStructure;

public class GreaterNumber {

    public static void main(String[] args) {

        int a = 20;//
        int b = 20;

        if(a>b)
        {
            System.out.println("a is greater");
        }
        else if (a==b)
        {
            System.out.println("a & b are equal");
        }
        else if (a<b)
        {
            System.out.println("b is greater");
        }
    }
}
